package entity;

/**
 *
 * @author asus
 */
public class MenuArticle {
    int id;
    String price;
    String name;
    String content;

    public MenuArticle() {
    }

    public MenuArticle(int id, String price, String name, String content) {
        this.id = id;
        this.price = price;
        this.name = name;
        this.content = content;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    
    
}
